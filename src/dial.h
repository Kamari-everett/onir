#pragma once

#include "onir.h"

class DialDevice;

class Dial {
public:
  Dial();
  Dial(int ch);
  Dial(Interface pinout);

  void attach(DialDevice* d);
  
  void set_channel(int ch) {
    channel = ch;
  }

  void update();
  bool press();
  bool release();

  int value() const {
    return state.count - zero_offset;
  }
  
  int down_value() const {
    return state.down_count - down_offset;
  }

  bool button() const {
    return state.button;
  }

  void zero() {
    zero_offset = state.count;
    down_offset = state.down_count;
  }

  DialState state;

private:
  int channel = -1;  // -1 means unset
  int zero_offset = 0;
  int down_offset = 0;
  
  bool press_ready = false;
  bool release_ready = false;

  DialDevice* device = nullptr;
};
